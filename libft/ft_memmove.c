#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = n;
	if (!dest & !src)
		return (dest);
	if (dest >= src)
	{
		while (i)
		{
			i--;
			*(char *)(dest + i) = *(char *)(src + i);
		}
	}
	else
	{
		i = 0;
		while (i < n)
		{
			*(char *)(dest + i) = *(char *)(src + i);
			i++;
		}
	}
	return (dest);
}
